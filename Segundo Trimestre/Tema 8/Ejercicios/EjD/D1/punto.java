package EjD.D1;

public class punto {
  int x;
  int y;
  public punto(int x, int y) {
    this.x=x;
    this.y=y;
  }
  /**
   * @return the x
   */
  public int getX() {
    return x;
  }
  /**
   * @return the y
   */
  public int getY() {
    return y;
  }
  /**
   * @param x the x to set
   */
  public void setX(int x) {
    this.x = x;
  }
  /**
   * @param y the y to set
   */
  public void setY(int y) {
    this.y = y;
  }

}
